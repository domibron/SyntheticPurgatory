public enum CompassDirection
{
    /// <summary>
    /// Positive Z
    /// </summary>
    North = 0,
    /// <summary>
    /// Positive X
    /// </summary>
    East = 1,
    /// <summary>
    /// Negative Z
    /// </summary>
    South = 2,
    /// <summary>
    /// Negative X
    /// </summary>
    West = 3,
}

